package exemplo.login;

public class Usuario {
  
  private String login;

  /**
   * Construtor.
   * @param login
   */
  public Usuario(String login) {
    super();
    this.login = login;
  }

  /**
   * Retorna o valor da propriedade login.
   * 
   * @return {@link #login}
   */
  public String getLogin() {
    return login;
  }
  
  

}
